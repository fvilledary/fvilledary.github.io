function WeekDay(num) {
  switch (num) {
    case 0:
      return 'Dom.';
      break;
    case 1:
      return 'Lun.';
      break;
    case 2:
      return 'Mar.';
      break;
    case 3:
      return 'Mié.';
      break;
    case 4:
      return 'Jue.';
      break;
    case 5:
      return 'Vie.';
      break;
    case 6:
      return 'Sáb.';
      break;
  }
}

